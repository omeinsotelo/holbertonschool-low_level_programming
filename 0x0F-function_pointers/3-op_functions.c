/**
 * op_add - function for add two elements
 * @a: the parameter
 * @b: second parameter
 *
 * Return: the add two elements
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function for sub two elements
 * @a: the parameter
 * @b: the second parameter
 *
 * Return: the sub of two elements.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function mul
 * @a: the parameter
 * @b: the second parameter
 *
 * Return: the mul of two elements
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to div two elements
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: the div of two parameter
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function returns the remainder of the division
 * @a: the first parameter
 * @b: the second parameter
 *
 * Return: mod of two parameter
 */
int op_mod(int a, int b)
{
	return (a % b);
}
